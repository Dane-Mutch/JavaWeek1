
public class TemperatureChecker {
	public static void main(String[] args) {
		temperature(70, true);
	}
	private static void temperature(int a, boolean isSummer) {
		if (isSummer = (true)) {
			if (a >=60 && a<=100) {
				System.out.println("True");
			} else {
				System.out.println("False");
			}
		}	else {
				if (a >= 60 && a<=90) {
					System.out.println("True");
				} else {
					System.out.println("False");
				}
		}
	
	}
}
